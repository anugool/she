        ชีลองส์ไม่ได้ให้การรับรอง หรือยืนยันความถูกต้องในเนื้อหาของเว็บไซต์เหล่านั้น
        การเข้าเยี่ยมชมหรือใช้ข้อมูลจากเว็บไซต์อื่น ๆ นั้น
        ถือเป็นหน้าที่ของคุณในการพิจารณาวิเคราะห์ความเสี่ยงที่อาจเกิดขึ้นจากการใช้ข้อมูลในเว็บไซต์เหล่านั้นด้วยตนเอง
        การเข้าเยี่ยมชมเว็บไซต์อื่น ๆ นั้น เราขอแนะนำให้คุณอ่านรายละเอียดเงื่อนไขและข้อตกลงการใช้บริการ
        ตลอดจนนโยบายการรักษาความปลอดภัยของเว็บไซต์เหล่านั้นก่อนเข้าใช้เว็บไซต์</div>

    <h1 style="padding-top:10px; font-size: 30px;">การลงทะเบียนและการสมัครสมาชิก</h1>
    <div style="text-indent: 1em;">
        คุณต้องมีอายุไม่ต่ำกว่า 18 ปี ในกรณีที่คุณอายุไม่ถึง 18 ปี และต้องการสมัครเป็นสมาชิกของชีลองส์
        คุณต้องได้รับการยินยอมจากบิดามารดาหรือผู้ปกครองของคุณเท่านั้น
        โดยแนบหนังสือยินยอมพร้อมสำเนาบัตรประชาชนของคุณเพื่อยอมรับเงื่อนไขการสมัครสมาชิกบนเว็บไซต์นี้</div>

    <div style="text-indent: 1em; font-size: 30px;">
        ผู้ใช้งานแต่ละคนจะได้รับอนุญาตให้มีบัญชีผู้ใช้ได้เพียงบัญชีเดียวเท่านั้น คุณต้องยินยอมที่จะให้ข้อมูลจริง
        ถูกต้อง
