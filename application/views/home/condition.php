        ในการรับโปรโมชั่นดังกล่าวหากสมาชิกที่ได้รับรางวัลจากชีลองส์
        มีการสมัครสมาชิกซ้ำซ้อนเอาไว้ในหลายชื่อหรือใช้อีเมลแอดเดรสหลายชื่อ
        รวมถึงการให้ข้อมูลเท็จหรือมีการกระทำเข้าข่ายหลอกลวง จะถูกตัดสิทธิในการได้รับรางวัลใด ๆ
        และอาจจะถูกดำเนินคดีตามกฎหมายหรือมีโทษปรับตามกฎหมายอาญา</div>

