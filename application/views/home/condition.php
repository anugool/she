        ทั้งที่เกี่ยวข้องกับนโยบายความเป็นส่วนตัว และความไม่ถูกต้องหรือความผิดพลาดของข้อมูลที่มีการนำเสนอบนเว็บไซต์
        รวมถึงการกระทำใด ๆ อันเกิดจากความเชื่อถือในข้อมูล</div>

