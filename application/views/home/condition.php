        ดังนี้เองคุณจึงเห็นชอบด้วยว่าข้อโต้แย้งหรือข้อเรียกร้องที่เกิดขึ้นจากเงื่อนไขและข้อตกลงนี้
        จะได้รับการแก้ไขด้วยศาลสูงที่ตั้งอยู่ในกรุงเทพมหานคร ประเทศไทยเท่านั้น
        และดังนี้เองคุณจึงได้อนุญาตและยอมรับต่ออำนาจศาลดังกล่าวในการฟ้องร้องข้อโต้แย้งหรือข้อเรียกร้องใด ๆ
        อันเกิดขึ้นจากการเกี่ยวพันกับเว็บไซต์ของเรา หรือเงื่อนไขในการให้บริการข้ออื่น ๆ ที่เกี่ยวเนื่องกัน
        รวมไปถึงประกาศนโยบายหรือเนื้อหาสาระข้อมูลอื่นใดที่มีความเกี่ยวข้องหรือมีความเกี่ยวโยงกัน</div>

