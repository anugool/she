        หรือไม่ครบถ้วนสมบูรณ์ ชีลองส์มีสิทธิ์ที่จะระงับการเป็นสมาชิกชั่วคราว ตลอดจนยกเลิกการเป็นสมาชิกของคุณ
        หรือปฏิเสธการสมัครสมาชิกเพื่อใช้งานของคุณอีกในอนาคต
        คุณจะต้องเป็นผู้รับผิดชอบแต่ผู้เดียวในการเก็บรักษาชื่อบัญชีผู้ใช้ และรหัสผ่านของตนเองให้เป็นความลับ
        คุณตกลงยินยอมที่จะรับผิดชอบต่อกิจกรรมใด ๆ ที่เกิดขึ้นภายใต้ชื่อบัญชีผู้ใช้งานของคุณเอง</div>

    <div style="text-indent: 1em; font-size: 30px;">
