        หากคุณเข้าสู่เว็บไซต์ หรือลงชื่อเข้าใช้เว็บไซต์ โดยใช้ข้อมูลในการเข้าสู่ระบบ (login credentials)
        จากเครือข่ายสังคมออนไลน์ของคุณ หรือใช้ฟีเจอร์เครือข่ายสังคมออนไลน์อื่นๆ เช่น วิดเจ็ต (widgets) ปลั๊กอิน
        (plug-ins)
        และการแจ้งเตือนของเบราเซอร์ (browser push notifications) ที่มีบนหน้าเว็บไซต์ของเรา
        การเข้าใช้เว็บไซต์ในลักษณะดังกล่าวอาจส่งผลให้ข้อมูลหรือข้อมูลส่วนบุคคลของคุณถูกเก็บรวบรวมหรือแบ่งปันระหว่างเราและบุคคลที่สาม
