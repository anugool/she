        และอาจแสดงโพสท์ข้อมูลดังกล่าวของคุณบน Facebook

        เว็บไซต์เครือข่ายสังคม (Social Networking Site)” หมายถึง
        แพลตฟอร์มออนไลน์หรือดิจิทัลที่บุคคลที่สามเป็นเจ้าของหรือเป็นผู้ดำเนินการโดยบุคคลทั่วไปสามารถใช้สร้างเครือข่ายสังคม
