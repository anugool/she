        เข้าถึงตำแหน่งของคุณ เพื่อแจ้งเตือนข้อเสนอพิเศษเฉพาะสถานที่ให้แก่คุณ หากคุณเลือก “ตกลง” แอพฯ
        จะสื่อสารกับอุปกรณ์เคลื่อนที่ของคุณและรวบรวมข้อมูลบางอย่างตามที่กำหนดไว้ในนโยบายฉบับนี้
        เพื่อส่งข้อเสนอที่ตรงเป้าหมายให้ตามตำแหน่งของคุณ
        คุณสามารถเลือกแสดงเจตนาไม่ยินยอมแบ่งปันข้อมูลตำแหน่งให้กับแอพฯ
        โดยเปลี่ยนการตั้งค่าอุปกรณ์ของคุณ</div>


