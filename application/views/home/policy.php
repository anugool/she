        คุกกี้ (Cookies) ชีลองส์อาจฝากคุกกี้ไว้ในคอมพิวเตอร์หรืออุปกรณ์เคลื่อนที่ของคุณเพื่อระบุตัวตนคุณ
        คุกกี้เป็นไฟล์ขนาดเล็กที่ถูกส่งออกจากเซิร์ฟเวอร์ระหว่างช่วงเวลาการค้นดูข้อมูล
        โดยธรรมดาแล้วคุกกี้จะถูกเก็บไว้บนฮาร์ดไดร์ฟของคอมพิวเตอร์
        และถูกใช้โดยเว็บไซต์เพื่อลอกเลียนการเชื่อมต่ออย่างต่อเนื่องไปยังเว็บไซต์
        มาตรการรักษาความปลอดภัยได้ถูกนำมาใช้เพื่อป้องกันการเข้าถึงข้อมูลของผู้เยี่ยมชมเว็บไซต์โดยไม่ได้รับอนุญาต
        อย่างไรก็ตาม
        ชีลองส์ไม่ได้ควบคุมการถ่ายโอนข้อมูลไปยังผู้ให้บริการด้านโทรคมนาคมซึ่งรวมถึงผู้ให้บริการอินเตอร์เน็ต
        ดังนั้น ชีลองส์จะไม่รับผิดชอบต่อการทำผิดต่อการรักษาความปลอดภัยหรือการเปิดเผยโดยไม่ได้รับอนุญาต
        หรือการใช้ข้อมูลใดๆ
        นั้นบนอินเตอร์เน็ต ซึ่งมิได้เกิดจากความผิดของชีลองส์ ทั้งนี้
        คุณสามารถปรับแต่งเบราเซอร์ของคุณเพื่อปฏิเสธคุกกี้ได้
        แต่อย่างไรก็ตาม การปรับแต่งนั้นอาจทำให้คุณไม่ได้รับประโยชน์อย่างเต็มที่จากการให้บริการหรือฟีเจอร์บนเว็บไซต์
        ในกรณีที่ข้อมูลที่เก็บรวบรวมโดยคุกกี้ดังกล่าวเป็นข้อมูลส่วนบุคคลของคุณ
