        ให้ถือว่าคุณตกลงยอมรับการใช้คุกกี้บนเว็บไซต์</div>

    <div style="text-indent: 1em; padding-top: 10px; font-size: 30px;">
        แฟลชคุกกี้ (Flash Cookies) เรียกอีกชื่อหนึ่งว่า Local Shared Objects หรือ “LSOs”
        เป็นไฟล์ข้อมูลที่คล้ายกับคุกกี้
        แต่สามารถจัดเก็บข้อมูลที่ซับซ้อนได้มากขึ้น แฟลชคุกกี้ใช้เพื่อจดจำการตั้งค่า การกำหนดลักษณะ
        และการใช้งานโดยเฉพาะอย่างยิ่ง วิดีโอ เกมส์แบบโต้ตอบ (interactive gaming) และบริการอื่นๆ ที่คล้ายคลึงกัน</div>

    <div style="text-indent: 1em; padding-top: 10px; font-size: 30px;">
        เว็บบีคอน (Web Beacons) เป็นภาพกราฟิกขนาดเล็กบนหน้าเว็บ
        หรือในอีเมลที่สามารถใช้บันทึกหน้าเว็บและโฆษณาที่ผู้ใช้คลิก
        หรือติดตามประสิทธิภาพของการทำการตลาดทางอีเมล</div>

    <div style="text-indent: 1em; padding-top: 10px; font-size: 30px;">
        แท็กการวิเคราะห์ (Analytics Tags) ใช้เพื่อวิเคราะห์ว่า ลูกค้าของเราชอบทำอะไร
        และฟีเจอร์รวมถึงโฆษณาของเรามีประสิทธิภาพเพียงใด
        นอกจากนี้แท็กดังกล่าวยังช่วยเราในการกำหนดค่าการใช้งานเบราเซอร์และการซื้อสินค้าของคุณได้
        เราอาจใช้ข้อมูลที่เก็บรวบรวมผ่านทางแท็กวิเคราะห์หรือลิงก์ที่ติดตามร่วมกับข้อมูลส่วนบุคคลของคุณ นอกจากนี้
        เราอาจรวมข้อมูลส่วนบุคคลที่คุณให้กับเรากับข้อมูลส่วนบุคคลอื่นๆ (เช่น ประวัติการซื้อและข้อมูลประชากร)</div>

    <div style="text-indent: 1em; padding-top: 10px; font-size: 30px;">
        บันทึกเซิร์ฟเวอร์ของเว็บ (Web Server Logs)
        คือบันทึกกิจกรรมที่สร้างขึ้นโดยอุปกรณ์เคลื่อนที่หรือคอมพิวเตอร์ที่ให้บริการเว็บเพจที่คุณร้องขอไปยังเบราเซอร์ของคุณ
        ตัวอย่างเช่น บันทึกเซิร์ฟเวอร์ของเว็บอาจบันทึกคำค้นหาที่คุณป้อนหรือลิงก์ที่คุณคลิกเพื่อให้เว็บเพจปรากฏขึ้น
        บันทึกเซิร์ฟเวอร์ของเว็บอาจบันทึกข้อมูลเกี่ยวกับเบราเซอร์ของคุณ เช่น ที่อยู่ IP
        และคุกกี้ที่ตั้งค่าไว้ในเบราเซอร์ของคุณโดยเซิร์ฟเวอร์</div>

    <div style="text-indent: 1em; padding-top: 10px; font-size: 30px;">
        เทคโนโลยีบ่งชี้ตำแหน่งทางภูมิศาสตร์ (Geo-Location Technologies) หมายถึง
        เทคโนโลยีที่อนุญาตให้เราสามารถระบุตำแหน่งของคุณได้ เราอาจขอให้คุณระบุข้อมูลสถานที่ด้วยตนเอง (เช่น
        รหัสไปรษณีย์ของคุณ) หรือเปิดใช้งานอุปกรณ์เคลื่อนที่ของคุณเพื่อส่งข้อมูลตำแหน่งที่แม่นยำ ตัวอย่าง เช่น
        ในครั้งแรกที่คุณดาวน์โหลดแอพฯ คุณจะ/อาจถูกขอให้เลือกระหว่างการอนุญาตหรือไม่อนุญาตให้แอพฯ
